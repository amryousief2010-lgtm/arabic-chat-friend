export function normalizeRegion(raw?: string | null): string {
  if (!raw) return "خط خاص / غير محدد";
  const s = raw.trim();
  if (!s || s === "خط غير محدد" || s === "غير محدد") return "خط خاص / غير محدد";
  return s;
}
